// Định nghĩa một interface Employee với các phương thức sau:
public interface Employee {
  // getEmployeeID: Trả về mã nhân viên (unique cho mỗi nhân viên).
  public String getEmployeeID();

  // getEmployeeName: Trả về tên nhân viên.
  public String getEmployeeName();

  // calculateSalary: Tính lương thực tế của nhân viên.
  public double calculateSalary();
}
